#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char		*temp_s;
	size_t		i;

	if (s != 0 && n != 0)
	{
		i = 0;
		temp_s = (char *)s;
		while (i < n)
		{
			*temp_s = 0;
			++i;
			++temp_s;
		}
	}
}
