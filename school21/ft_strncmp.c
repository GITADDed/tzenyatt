int		ft_strncmp(char *str1, char *str2, int n_ch)
{
	int i;

	i = 0;
	while (*str1 != '\0' && *str2 != '\0' && i < n_ch)
	{
		if (*str1 != *str2)
			return (*str1 - *str2);
		++str1;
		++i;
		++str2;
	}
	return (*str1 - *str2);
}
